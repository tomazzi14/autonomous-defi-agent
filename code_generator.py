import logging

logger = logging.getLogger("code_gen")


def generate_solidity(job: dict) -> str:
    """Analyze a job and generate tailored Solidity code."""
    title = job.get("title", "").lower()
    desc = job.get("description", "").lower()
    text = f"{title} {desc}"

    if "hook" in text or "uniswap" in text:
        return _generate_uniswap_hook(job)
    elif "erc20" in text or "token" in text:
        return _generate_erc20(job)
    elif "erc1155" in text:
        return _generate_erc1155(job)
    elif "staking" in text or "stake" in text:
        return _generate_staking(job)
    elif "audit" in text or "security" in text or "review" in text:
        return _generate_audit_report(job)
    else:
        return _generate_generic_contract(job)


