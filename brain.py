def analyze_job_fit(job: dict) -> Optional[dict]:
    """Use Claude to deeply analyze if a job is a good fit and why."""
    client = _get_client()
    if not client:
        return None

    title = job.get("title", "")
    desc = job.get("description", "")[:1500]
    tags = ", ".join(job.get("tags", []))
    budget = job.get("budget_amount", "?")

    try:
        response = client.messages.create(
            model="claude-haiku-4-5-20251001",
            max_tokens=200,
            system=SYSTEM_PROMPT,
            messages=[
                {
                    "role": "user",
                    "content": f"""Analyze this job for defi_builder agent (DeFi/Solidity specialist).

Title: {title}
Description: {desc}
Tags: {tags}
Budget: {budget} NEAR

Reply in EXACTLY this format (no other text):
SCORE: 0.XX
REASON: one sentence why
APPROACH: one sentence how you'd tackle it""",
                }
            ],
        )
        text = response.content[0].text.strip()

        # Parse the structured response
        result = {}
        for line in text.split("\n"):
            if line.startswith("SCORE:"):
                try:
                    result["score"] = float(line.split(":")[1].strip())
                except ValueError:
                    result["score"] = 0.5
            elif line.startswith("REASON:"):
                result["reason"] = line.split(":", 1)[1].strip()
            elif line.startswith("APPROACH:"):
                result["approach"] = line.split(":", 1)[1].strip()

        return result if "score" in result else None
    except Exception as e:
        logger.warning("AI job analysis failed: %s", e)
        return None
